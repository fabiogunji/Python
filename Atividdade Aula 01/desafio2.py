# Faça um programa que leia um número inteiro e mostra na tela e seu sucessor e seu antecessor.

numero1 = int(input("Entre com o um número inteiro: "))

antecessor = numero1 -1
sucessor = numero1 + 1

print(f"O número inserido é {numero1}, sucessor dele é {sucessor} e o antecessor dele é {antecessor}")