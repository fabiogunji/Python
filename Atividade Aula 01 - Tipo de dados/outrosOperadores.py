# operador módulo (resto de uma divisão)
numero1 = int(input("Digite um valor: "))
numero2 = int(input("Digite outro valor: "))

modulo = numero1 % numero2
print(f"O módulo de {numero1} e {numero2} é {modulo}")

# operador divisão por inteiro (Soh pegao valor inteiro de uma divisão)
divisaoInteiro = numero1 // numero2
print(f"A divisão por inteiro de {numero1} e {numero2} é {divisaoInteiro}")

