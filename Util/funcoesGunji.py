def validaMaior(numero1, numero2):
    if (numero1 > numero2): 
        numeroMaior = numero1        
    else:             
        numeroMaior = numero2
    return numeroMaior
        
                
def validaMenor(numero1, numero2):
    if (numero1 < numero2): 
        numeroMenor = numero1        
    else:             
        numeroMenor = numero2 
    
    return numeroMenor